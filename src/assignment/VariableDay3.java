package assignment;

public class VariableDay3 {

    public static void main(String[] args) {


        double  a = 12.56;
        double b =4.4;

        System.out.println("variable a is :"+a);
        System.out.println("variable  b is:"+b);

        //square

        double square = a*a;
        System.out.println("Square of a is :" +square);


        //add

        double add = a+b;
         System.out.println("add of a and b is:" +add);

         //substract

        double sub = a-b;
        System.out.println("subtraction of a and b is:" +sub);

        //multiply

        double mul = a*b;
        System.out.println(" multiply of a and b is:" +mul);

        //division

        double div =a/b;
        System.out.println("division of a and b is :"+div);

        //mod

        double mod = a%b;
        System.out.println("remainder is :" +mod);


        //increment

        a++;
        System.out.println("increment of a is :"+a);

        b++;
        System.out.println("increment of b is :" +b);

        //decrement

        a--;
        System.out.println("decrement of a is :" +a);

        b--;
        System.out.println(" decrement of b is :"+b);


        char as ='Z';

        as--;
        System.out.println("decrement of char as variable is :" +as);



    }

}
